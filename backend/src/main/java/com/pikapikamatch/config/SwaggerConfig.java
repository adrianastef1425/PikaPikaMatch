package com.pikapikamatch.config;

import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.info.Contact;
import io.swagger.v3.oas.models.info.Info;
import io.swagger.v3.oas.models.servers.Server;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import java.util.List;

@Configuration
public class SwaggerConfig {
    
    @Value("${server.port:8080}")
    private String serverPort;
    
    @Bean
    public OpenAPI customOpenAPI() {
        return new OpenAPI()
                .info(new Info()
                        .title("PikaPikaMatch API")
                        .version("1.0.0")
                        .description("Backend API for PikaPikaMatch voting application. " +
                                "This API integrates data from Pokemon, Rick and Morty, and Superhero APIs, " +
                                "allowing users to vote on characters and view statistics.")
                        .contact(new Contact()
                                .name("PikaPikaMatch Team")
                                .email("support@pikapikamatch.com")
                                .url("https://github.com/pikapikamatch")))
                .servers(List.of(
                        new Server()
                                .url("http://localhost:" + serverPort)
                                .description("Local development server"),
                        new Server()
                                .url("https://api.pikapikamatch.com")
                                .description("Production server")
                ));
    }
}
